#include <stdio.h>

int main() {

    printf("Boa tarde! \n");
    printf("Valor inteiro: %d \n", 64);
    printf("Valor inteiro: %d \n", 5 * 5);
    printf("Valor inteiro: %d \n", 5 * (5 + 5));
    printf("Valor decimal: %.3f \n", 3.15915246);
    printf("Valor decimal: %f \n", 4.231);
    printf("Caractere: %c \n", 'A');
    printf("%d + %d = %d \n", 3, 5, 3 + 5);

    return 0;
}

/*Tipos de dados:
int - Inteiro(%d)
float ou double - Decimais(%f)
char - Caractere(%c) */